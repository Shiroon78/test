a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

print("The sum is: ", a, "+", b, "=", a + b)
print("The differnce is: ", a, "-", b, "=", a - b)
print("The multiplication is: ", a, "x", b, "=", a * b)
print("The division is: ", a, "/", b, "=", a / b)
print("The float division is: ", a, "//", b, "=", a // b)
print("The module operator is: ", a, "%", b, "=", a % b)
print("The exponential is: ", a, "^", b, "=", a ** b)