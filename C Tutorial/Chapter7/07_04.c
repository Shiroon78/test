#include<stdio.h>

int main()
{
    int a[] = {34,113,56};
    printf("Value of a[0] is %d\n", a[0]);
    printf("Value of a[1] is %d\n", a[1]);
    printf("Value of a[2] is %d\n", a[2]);

    float b[] = {34.56,113.99,56.01};
    printf("Value of b[0] is %f\n", b[0]);
    printf("Value of b[1] is %f\n", b[1]);
    printf("Value of b[2] is %f\n", b[2]);
    return 0;
}
